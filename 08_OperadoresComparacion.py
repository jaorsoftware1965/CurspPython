# Clase 08 Operadores de Comparación

# Los operadores de comparación en Python, son los tradicionales que existen en Matemáticas
# y en todos los lenguajes de programación.

# Estos operadores, a diferencia de los operadores aritméticos, el resultado que devuelven
# es un valor Booleano; es decir True o False.

# Los operadores de comparación que existen en Python son:
# 
# -------------------------------------------------------------------------------------------------------
# Operador  Nombre              Descripción                                     Ejemplos
# -------------------------------------------------------------------------------------------------------
#    ==     Igual               Compara si los operandos son iguales            x = 5 == 8; x vale False
#    !=     Distinto            Compara si los operandos son distintos          x = 5 != 8; x vale True
#    >      Mayor que           Compara si un operando es mayor que             x = 5 >  8; x vale False
#    >=     Mayor o Igual que   Compara si un operando es mayor o igual que     x = 5 >= 8; x vale False
#    <      Menor que           Compara si un operando es menor que             x = 5 <  8; x vale True
#    <=     Menor o Igual que   Compara si un operando es menor o igual que     x = 5 <= 8; x vale True

# Desplegamos Mensaje de la Clase
print("Clase 08 Operadores de Comparación")

# Declaramos variables numéricas
a = 5
b = 8

# Accedemos a subconjunto de caracteres
print ("a == b  = ",a == b)
print ("a != b  = ",a != b)
print ("a >  b  = ",a >  b)
print ("a >= b  = ",a >= b)
print ("a <  b  = ",a <  b)
print ("a <= b  = ",a <= b)


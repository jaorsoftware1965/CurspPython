# Clase 03 Identificadores

# Se le conoce como Identificadores a los nombres que se utilizan en programación
# para identificar a las variables, funciones, clases y cualquier otro objeto que necesite
# un nombre.

# Un Identificador debe iniciar con una Letra; A-Z o a-z; o con un Underscore(_) seguido
# de mas letras, mas underscore o numeros; del 0-9.

# Los Caracteres que no permite python que se utilicen en los identificadores, son los de
# puntuación y caracteres especiales como son: @, $ y %.

# Python diferencía de Mayúsculas y Minúsculas; por lo que un Identificador xIva es distinto
# de xIVA.


print("Clase 03 Identificadores")
xIva = 15
_xIVA = 25
print(xIva)
print(_xIVA)

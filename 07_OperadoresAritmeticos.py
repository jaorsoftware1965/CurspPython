# Clase 07 Operadores Aritméticos

# En todos los lenguajes de programación existen Operadores.
# Un operador es un caracter o un conjunto de caracteres que representan una operación.

# Los Operadores que veremos en esta Clase son los Operadores Aritméticos.
# Como su nombre lo indica, nos pemitiran realizar operaciones matemáticas; y por lo tanto
# el resultado que devolverán será un valor numérico.
# 
# Los operadores aritméticos que existen en Python son:
# 
# -----------------------------------------------------------------------------------------
# Operador  Nombre              Descripción                 Ejemplos
# -----------------------------------------------------------------------------------------
#    +      Suma                Operación de Suma           x =  5 + 10;  x vale 15
#    -      Resta               Operación de Resta          x = 15 - 10;  x vale 5
#    *      Multiplica          Operación de Multiplicar    x =  5 *  2;  x vale 10
#    /      Divide              Operación de Dividir        x =  10 / 4;  x vale 2.5
#    %      Módulo              Residuo de la División      x =  10 % 3;  x vale 1
#    //     División Entera     Cociente de la División     x =  10 // 3; x vale 3
#    **     Exponente           Operación de Potencia       x =   2 ** 4; x vale 16

# Desplegamos Mensaje de la Clase
print("Clase 07 Operadores Artiméticos")

# Declaramos variables numéricas
a = 10
b = 3

# Accedemos a subconjunto de caracteres
print ("a + b  = ",a + b)
print ("a - b  = ",a - b)
print ("a * b  = ",a * b)
print ("a / b  = ",a / b)
print ("a % b  = ",a % b)
print ("a // b = ",a // b)
print ("a ** b = ",a ** b)
